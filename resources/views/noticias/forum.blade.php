    <h2>PS5 deve superar Xbox Series em vendas em até 2 anos, diz analista</h2>
    <p>A disputa entre PlayStation 5 e Xbox Series promete ser acirrada ao longo dos próximos anos. De acordo com Piers Harding-Rolls, diretor de pesquisa da empresa Ampere, a Sony deve levar a dianteira nessa disputa em um ou dois anos.

Para o analista, as projeções são de que sejam vendidas 18 milhões de unidades do PlayStation 5 ao longo deste ano, e 9 milhões de unidades para os videogames da Microsoft.

"Parece que a disponibilidade do PlayStation 5 e do Xbox Series deve aumentar de maneira progressiva ao longo do ano, e nossas expectativas são de que a Sony supere a Microsoft em vendas em um ou dois anos", comentou Harding-Rolls.</p>
